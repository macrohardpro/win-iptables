TEST(PersistAsync, SaveAsyncReturnsValidFuture) {
    TempFile tmp;
    RuleStore store;
    auto fut = RulePersist::save_async(store, tmp.path);
    EXPECT_TRUE(fut.valid());
    bool ok = fut.get();
    EXPECT_TRUE(ok);
    EXPECT_TRUE(fs::exists(tmp.path));
}

TEST(PersistAsync, SaveAsyncWritesCorrectContent) {
    TempFile tmp;
    RuleStore store;
    store.append_rule(TableKind::kFilter, "INPUT", make_rule(Target::Kind::kDrop));
    auto fut = RulePersist::save_async(store, tmp.path);
    bool ok = fut.get();
    EXPECT_TRUE(ok);
    std::ifstream ifs(tmp.path);
    std::string content((std::istreambuf_iterator<char>(ifs)),
                         std::istreambuf_iterator<char>());
    EXPECT_NE(content.find("-A INPUT -j DROP"), std::string::npos);
}

TEST(PersistAsync, SaveAsyncDoesNotBlockCaller) {
    TempFile tmp;
    RuleStore store;
    auto start = std::chrono::steady_clock::now();
    auto fut = RulePersist::save_async(store, tmp.path);
    auto elapsed = std::chrono::steady_clock::now() - start;
    EXPECT_LT(std::chrono::duration_cast<std::chrono::milliseconds>(elapsed).count(),
              500);
    fut.get();
}
