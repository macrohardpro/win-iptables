TEST(PersistRoundTrip, EmptyStoreRoundTrip) {
    TempFile tmp;
    RuleStore store1;
    ASSERT_TRUE(RulePersist::save(store1, tmp.path));
    RuleStore store2;
    ASSERT_TRUE(RulePersist::load(store2, tmp.path));
    for (const char* chain : {"INPUT", "OUTPUT", "FORWARD"}) {
        const Chain* c1 = store1.get_chain(TableKind::kFilter, chain);
        const Chain* c2 = store2.get_chain(TableKind::kFilter, chain);
        ASSERT_NE(c1, nullptr);
        ASSERT_NE(c2, nullptr);
        ASSERT_TRUE(c1->policy.has_value());
        ASSERT_TRUE(c2->policy.has_value());
        EXPECT_EQ(c1->policy->kind, c2->policy->kind)
            << "Policy mismatch for chain " << chain;
    }
}

TEST(PersistRoundTrip, RulesPreservedAfterRoundTrip) {
    TempFile tmp;
    RuleStore store1;
    store1.append_rule(TableKind::kFilter, "INPUT",   make_rule(Target::Kind::kDrop));
    store1.append_rule(TableKind::kFilter, "INPUT",   make_rule(Target::Kind::kAccept));
    store1.append_rule(TableKind::kFilter, "FORWARD", make_rule(Target::Kind::kDrop));
    ASSERT_TRUE(RulePersist::save(store1, tmp.path));
    RuleStore store2;
    ASSERT_TRUE(RulePersist::load(store2, tmp.path));
    EXPECT_EQ(rule_count(store2, TableKind::kFilter, "INPUT"),   2u);
    EXPECT_EQ(rule_count(store2, TableKind::kFilter, "FORWARD"), 1u);
    EXPECT_EQ(rule_count(store2, TableKind::kFilter, "OUTPUT"),  0u);
}

