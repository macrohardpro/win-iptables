// Temporary file RAII wrapper
struct TempFile {
    std::string path;
    explicit TempFile(const std::string& suffix = "v4") {
        auto tmp = fs::temp_directory_path() / ("winiptables_test_" + suffix);
        path = tmp.string();
    }
    ~TempFile() {
        std::error_code ec;
        fs::remove(path, ec);
    }
};

// Build a simple rule
static Rule make_rule(Target::Kind kind = Target::Kind::kAccept) {
    Rule r;
    r.target.kind = kind;
    return r;
}

// Build a JUMP rule
static Rule make_jump_rule(const std::string& chain) {
    Rule r;
    r.target.kind       = Target::Kind::kJump;
    r.target.jump_chain = chain;
    return r;
}

// Count rules in a given table/chain
static size_t rule_count(const RuleStore& store, TableKind table,
                          const std::string& chain) {
    const Chain* c = store.get_chain(table, chain);
    if (!c) return 0;
    return c->rules.size();
}

