TEST(PersistRoundTrip, RuleTargetKindPreservedAfterRoundTrip) {
    TempFile tmp;
    RuleStore store1;
    store1.append_rule(TableKind::kFilter, "INPUT",   make_rule(Target::Kind::kDrop));
    store1.append_rule(TableKind::kFilter, "OUTPUT",  make_rule(Target::Kind::kAccept));
    store1.append_rule(TableKind::kFilter, "FORWARD", make_rule(Target::Kind::kReturn));
    ASSERT_TRUE(RulePersist::save(store1, tmp.path));
    RuleStore store2;
    ASSERT_TRUE(RulePersist::load(store2, tmp.path));
    auto get_first_target = [&](TableKind t, const std::string& chain) {
        const Chain* c = store2.get_chain(t, chain);
        if (!c || c->rules.empty()) return Target::Kind::kAccept;
        return c->rules.front()->target.kind;
    };
    EXPECT_EQ(get_first_target(TableKind::kFilter, "INPUT"),   Target::Kind::kDrop);
    EXPECT_EQ(get_first_target(TableKind::kFilter, "OUTPUT"),  Target::Kind::kAccept);
    EXPECT_EQ(get_first_target(TableKind::kFilter, "FORWARD"), Target::Kind::kReturn);
}

